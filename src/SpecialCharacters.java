public class SpecialCharacters {
    public static void main(String[] args) {
        // special characters
        // '\n' - print a newline character
        var string1 = "Hello\nWorld";
        System.out.println("string1 = " + string1);

        // '\t' - print a tab character
        var string2 = "\tHello\tWorld";
        System.out.println("string2 = " + string2);

        //  '\'' - adds a single quotation mark
        var string3 = "Hello \' World";
        System.out.println("string3 = " + string3);

        //  '\"' - adds a double quotation mark
        var string4 = "Hello \" World";
        System.out.println("string4 = " + string4);

        // '\\' - backslash character
        var string5 = "Hello \\ World";
        System.out.println("string5 = " + string5);


    }
}
